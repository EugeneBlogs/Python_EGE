# Условие задания КИМ 14 (Умскул):
'''
В выражении "ABCD3y2x1 (14сс) + 192x9 (14сс)" переменные "х" и "у" обозначают некоторые цифры из алфавита системы счисления с основанием 14.
Определите наименьший "х", для которого есть такой "у", при котором значение данного выражения кратно 107.
При найденных значениях "х" и "у" определите частное от деления выражения на 107 и укажите его в ответе в десятичной системе счисления.
'''

import string

result = ""
base = 14
alpabet = string.digits + string.ascii_uppercase
alpabet = alpabet[:base]
for x in alpabet:
    for y in alpabet:
        summa = int(f"ABCD3{y}2{x}1", base) + int(f"192{x}9", base)
        if summa % 107 == 0:
            result = summa // 107
            break
    if result:
        print(result)
        break

# Условие задания КИМ 14 (Умскул):
'''
Операнды арифметического выражения записаны в системе счисления с основанием 16 и 12:
"32Dx (16cc) + 43xB (12cc)"
В записи чисел переменной "х" обозначены допустимые в данных системах счисления неизвестные цифры.
Вам необходимо найти такое значение "х", при котором данное выражения будет кратно 15,
и указать в ответе значение старшего разряда полученной суммы в десятичной системе.
'''

import string

result = ""
base = min(16, 12)
alpabet = string.digits + string.ascii_uppercase
alpabet = alpabet[:base]
for x in alpabet:
    summa = int(f"32D{x}", 16) + int(f"43{x}B", 12)
    if summa % 15 == 0:
        print(str(summa)[0])
        break

