# Условие задания КИМ 14 (Умскул):
'''
Значение выражения 3*3125^8 + 2*625^7 - 4*625^6 + 3*125^5 - 2*25^4 - 2025 записали в системе счисления с основанием 25.
Сколько значащих нулей в двоичной записи этого числа?
'''

x = 3 * 3125 ** 8 + 2 * 625 ** 7 - 4 * 625 ** 6 + 3 * 125 ** 5 - 2 * 25 ** 4 - 2025
count_0 = 0
while x > 0:
    if x % 25 == 0:
        count_0 += 1
    x //= 25
print(count_0)
