'''
Текстовый файл состоит из символов латинского алфавита в нижнем регистре ("a—z") и цифр ("0-9").
Определите в прилагаемом файле максимальную длину подстроки (непрерывной подпоследовательности),
которая состоит из повторяющегося слова "yandex". Последнее слово в цепочке может быть неполным.
По правилам "leet" ("1337") символ "a" может быть заменён на символ "4", а символ "e" — на символ "3".
'''

f = open("9.txt")
s = f.readline()
# Шаблон слова "yandex" с учетом замен 'a' на '4' и 'e' на '3'
pattern = ['y', ['a', '4'], 'n', 'd', ['e', '3'], 'x']
max_len = 0  # Максимальная длина подстроки
current_len = 0  # Текущая длина подстроки
pos_in_pattern = 0  # Текущая позиция в шаблоне

for char in s:
    # Ожидаемый символ в текущей позиции шаблона
    expected = pattern[pos_in_pattern % len(pattern)]
    # Проверяем, совпадает ли текущий символ с ожидаемым (с учётом замен)
    if (isinstance(expected, list) and (char in expected)) or (char == expected):
        current_len += 1
        pos_in_pattern += 1
    else:
        # Если не совпадает, сохраняем максимум и сбрасываем счетчик
        max_len = max(max_len, current_len)
        current_len = 0
        pos_in_pattern = 0
        # Проверяем, может ли текущий символ начать новое слово "yandex"
        if char == 'y':
            current_len = 1
            pos_in_pattern = 1

# Проверяем последнюю подстроку
max_len = max(max_len, current_len)
print(max_len)
